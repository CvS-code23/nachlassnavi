const PLZ_PREFIX_MAP = {};
PLZ_REGIONEN.forEach(r => { PLZ_PREFIX_MAP[r[0]] = r; });
const PLZ_FALLBACK = ["00","Ländlich",60,300,700,2500,5.5,9.0,79,88];

function hashPLZ(plz) {
  let h = 0;
  for (let i = 0; i < plz.length; i++) {
    h = Math.imul(h, 31) + plz.charCodeAt(i) | 0;
    h = h >>> 0;
  }
  return h;
}

function getPLZData(plz) {
  if (!plz || plz.length !== 5) return null;
  const r = PLZ_PREFIX_MAP[plz.slice(0,2)] || PLZ_FALLBACK;
  const [pfx, label, brw_lo, brw_hi, kauf_lo, kauf_hi, miete_lo, miete_hi, kf_lo, kf_hi] = r;
  const h = hashPLZ(plz);
  const t_brw   = ((h >>> 0) & 0xFF) / 255;
  const t_kauf_raw = ((h >>> 8) & 0xFF) / 255;
  const t_miete_raw = ((h >>> 16) & 0xFF) / 255;
  const t_kf    = ((h >>> 24) & 0xFF) / 255;
  const t_kauf  = t_kauf_raw * 0.5 + t_brw * 0.5;
  const t_miete = t_miete_raw * 0.5 + t_brw * 0.5;
  const lerp = (a, b, t) => a + (b-a)*t;
  return {
    stadt: label,
    brw:   Math.round(lerp(brw_lo, brw_hi, t_brw)),
    kauf:  Math.round(lerp(kauf_lo, kauf_hi, t_kauf)),
    miete: Math.round(lerp(miete_lo, miete_hi, t_miete) * 2) / 2,
    kf:    Math.round(lerp(kf_lo, kf_hi, t_kf))
  };
}
